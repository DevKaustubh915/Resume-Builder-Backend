package com.example.resumebuilderapi.repository;


import com.example.resumebuilderapi.document.Payment;
import org.springframework.data.mongodb.repository.MongoRepository;

import java.util.List;
import java.util.Optional;

public interface PaymentRepository extends MongoRepository<Payment,String> {


    Optional<Payment> findPaymentByRazorpayOrderId(String razorpayOrderId);

    Optional<Payment> findPaymentByRazorpayPaymentId(String razorpayPaymentId);

    List<Payment> findPaymentsByUserIdOrderByCreatedAtDesc(String userId);


    List<Payment> findByStatus(String status);



}
